@dataclass(frozen=True)
class SampleRecord:
    code: str
    comment: str
    language: str
    repo: str
    path: str
    source: str
    code_type: str = "function"  # function | loop | complex_logic | variable


# ── Pre-tokenization ────────────────────────────────────────────────────────

def _pre_tokenize(text: str) -> list[str]:
    """
    Split raw text into coarse word-level tokens before BPE.
    Handles camelCase, snake_case, punctuation, and whitespace.
    """
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    chunks: list[str] = []
    for segment in re.findall(r"[A-Za-z_][A-Za-z0-9_]*|[0-9]+|[^\s\w]|\n", text):
        if re.match(r"^[A-Za-z_][A-Za-z0-9_]*$", segment):
            parts = re.sub(r"([a-z])([A-Z])", r"\1 \2", segment).split()
            for p in parts:
                for sub in p.split("_"):
                    if sub:
                        chunks.append(sub.lower())
        else:
            chunks.append(segment)
    return chunks


# ── BPE Tokenizer ───────────────────────────────────────────────────────────

class BPETokenizer:
    """
    Custom Byte-Pair Encoding tokenizer.

    Training:
        1. Pre-tokenize corpus into coarse words.
        2. Split each word into characters → build initial vocabulary.
        3. Iteratively merge the most frequent adjacent pair until vocab_size is reached.

    Encoding:
        Apply learned merges greedily to produce subword token ids.

    Decoding:
        Map ids back to subword strings and join with spaces (stripping special tokens).
    """

    def __init__(self):
        self.token2id: dict[str, int] = {}
        self.id2token: dict[int, str] = {}
        self.merges: list[tuple[str, str]] = []
        self._merge_set: set[tuple[str, str]] = set()
        self._encode_word_cache: dict[str, list[int]] = {}

    @property
    def vocab_size(self) -> int:
        return len(self.token2id)

    @property
    def pad_id(self) -> int:
        return self.token2id[PAD_TOKEN]

    @property
    def sos_id(self) -> int:
        return self.token2id[SOS_TOKEN]

    @property
    def eos_id(self) -> int:
        return self.token2id[EOS_TOKEN]

    @property
    def unk_id(self) -> int:
        return self.token2id[UNK_TOKEN]

    # ── Training ─────────────────────────────────────────────────────────

    def train(self, corpus: list[str], target_vocab_size: int = BPE_VOCAB_SIZE):
        """
        Train BPE on the given corpus of raw text strings.
        """
        print(f"  [BPE] Pre-tokenizing {len(corpus)} documents …")
        word_freq: Counter[tuple[str, ...]] = Counter()
        for doc in corpus:
            for word in _pre_tokenize(doc):
                chars = tuple(word)
                word_freq[chars] += 1

        # Initialize vocabulary with special tokens + individual characters
        self.token2id = dict(SPECIAL_IDS)
        self.id2token = {v: k for k, v in self.token2id.items()}
        next_id = len(SPECIAL_IDS)

        char_set: set[str] = set()
        for word in word_freq:
            for ch in word:
                char_set.add(ch)
        for ch in sorted(char_set):
            if ch not in self.token2id:
                self.token2id[ch] = next_id
                self.id2token[next_id] = ch
                next_id += 1

        # Build split representation:  word → list of current subword tokens
        splits: dict[tuple[str, ...], list[str]] = {
            word: list(word) for word in word_freq
        }

        self.merges = []
        num_merges = target_vocab_size - len(self.token2id)
        print(f"  [BPE] Learning {num_merges} merges (target vocab: {target_vocab_size}) …")

        for merge_i in range(num_merges):
            # Count adjacent pairs
            pair_freq: Counter[tuple[str, str]] = Counter()
            for word, freq in word_freq.items():
                toks = splits[word]
                for j in range(len(toks) - 1):
                    pair_freq[(toks[j], toks[j + 1])] += freq

            if not pair_freq:
                break

            best_pair = pair_freq.most_common(1)[0][0]
            merged_token = best_pair[0] + best_pair[1]

            self.merges.append(best_pair)
            if merged_token not in self.token2id:
                self.token2id[merged_token] = next_id
                self.id2token[next_id] = merged_token
                next_id += 1

            # Apply merge to all words
            for word in splits:
                toks = splits[word]
                new_toks: list[str] = []
                i = 0
                while i < len(toks):
                    if i < len(toks) - 1 and (toks[i], toks[i + 1]) == best_pair:
                        new_toks.append(merged_token)
                        i += 2
                    else:
                        new_toks.append(toks[i])
                        i += 1
                splits[word] = new_toks

            if (merge_i + 1) % 500 == 0:
                print(f"    merge {merge_i + 1}/{num_merges}  vocab={len(self.token2id)}")

        self._merge_set = set(self.merges)
        self._encode_word_cache.clear()
        print(f"  [BPE] Done. Final vocab size: {self.vocab_size}")

    # ── Encoding / Decoding ──────────────────────────────────────────────

    def _encode_word(self, word: str) -> list[int]:
        """Encode a single pre-tokenized word; cache to avoid repeated merge passes."""
        cached = self._encode_word_cache.get(word)
        if cached is not None:
            return cached

        subwords = list(word)
        for a, b in self.merges:
            merged = a + b
            new: list[str] = []
            i = 0
            while i < len(subwords):
                if i < len(subwords) - 1 and subwords[i] == a and subwords[i + 1] == b:
                    new.append(merged)
                    i += 2
                else:
                    new.append(subwords[i])
                    i += 1
            subwords = new

        word_ids = [self.token2id.get(sw, self.unk_id) for sw in subwords]
        self._encode_word_cache[word] = word_ids
        return word_ids

    def encode(self, text: str, max_len: int | None = None, add_eos: bool = False) -> list[int]:
        """Encode raw text string into a list of integer token ids."""
        words = _pre_tokenize(text)
        ids: list[int] = []
        for word in words:
            ids.extend(self._encode_word(word))

        if add_eos:
            ids.append(self.eos_id)
        if max_len is not None:
            ids = ids[:max_len]
        return ids

    def decode(self, ids: list[int] | Iterator[int], skip_special: bool = True) -> str:
        """Decode a list of token ids back into a human-readable string."""
        special = {self.pad_id, self.sos_id, self.eos_id}
        tokens: list[str] = []
        for idx in ids:
            if skip_special and idx in special:
                continue
            tokens.append(self.id2token.get(idx, UNK_TOKEN))
        return " ".join(tokens)

    # ── Serialization ────────────────────────────────────────────────────

    def save(self, path: str):
        """Persist tokenizer state (vocab + merges) to JSON."""
        data = {
            "token2id": self.token2id,
            "merges": [list(m) for m in self.merges],
        }
        with open(path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False)
        print(f"  [BPE] Saved tokenizer → {path}")

    @classmethod
    def load(cls, path: str) -> "BPETokenizer":
        """Load a previously saved tokenizer from JSON."""
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
        tok = cls()
        tok.token2id = data["token2id"]
        tok.id2token = {int(v): k for k, v in tok.token2id.items()}
        # rebuild id2token properly - keys are tokens, values are ids
        tok.id2token = {v: k for k, v in tok.token2id.items()}
        tok.merges = [tuple(m) for m in data["merges"]]
        tok._merge_set = set(tok.merges)
        tok._encode_word_cache = {}
        return tok


# ── Formatting Pipe ──────────────────────────────────────────────────────────

class FormattingPipe:
    """
    Wraps (code, comment) pairs into the GPT prompt template.
    Training:   "Code:\n[TYPE:xxx]\n{code}\n\nComment: {comment}<eos>"
    Inference:  "Code:\n[TYPE:xxx]\n{code}\n\nComment:"  (model generates from here)

    The [TYPE:xxx] tag tells the model what kind of code element it is
    commenting on: function, loop, complex_logic, or variable.
    """

    PROMPT_TEMPLATE = "Code:\n[TYPE:{code_type}]\n{code}\n\nComment:"
    FULL_TEMPLATE = "Code:\n[TYPE:{code_type}]\n{code}\n\nComment: {comment}"

    @staticmethod
    def format_train(code: str, comment: str, code_type: str = "function") -> str:
        return FormattingPipe.FULL_TEMPLATE.format(code=code, comment=comment, code_type=code_type)

    @staticmethod
    def format_inference(code: str, code_type: str = "function") -> str:
        return FormattingPipe.PROMPT_TEMPLATE.format(code=code, code_type=code_type)


# ── Data Collection (from CodeSearchNet) ─────────────────────────────────────

def _extract_summary(doc: str) -> str:
    """Extract a richer explanatory summary while preserving punctuation and structure."""
    cleaned = doc.replace("\r\n", "\n").replace("\r", "\n")
    cleaned = re.sub(r"```.*?```", " ", cleaned, flags=re.S)
    cleaned = re.sub(r"<code>(.*?)</code>", r"\1", cleaned, flags=re.I | re.S)
    cleaned = re.sub(r"</?\w+[^>]*>", " ", cleaned)
    cleaned = re.sub(r"@(?:param|return|returns|args?|example|examples?|throws?|raise)\b[^\n]*", " ", cleaned, flags=re.I)
    cleaned = re.sub(r"[\t ]+", " ", cleaned)

    paragraphs = [p.strip() for p in re.split(r"\n\s*\n", cleaned) if p.strip()]
    if not paragraphs:
        return ""

    # Prefer the first two prose paragraphs so summaries can capture line-by-line intent.
    candidate_text = " ".join(paragraphs[:2])
    candidate_text = re.sub(r"\s+", " ", candidate_text).strip()
    if not candidate_text:
        return ""

    sentences = [s.strip() for s in re.split(r"(?<=[.!?;:])\s+", candidate_text) if s.strip()]
    if not sentences:
        return ""

    min_words, max_words, target_words = 5, 20, 15
    chosen: list[str] = []
    word_count = 0

    for s in sentences:
        s_words = re.findall(r"[A-Za-z0-9_']+", s)
        if not s_words:
            continue
        next_count = word_count + len(s_words)
        if next_count > max_words and word_count >= min_words:
            break
        chosen.append(s)
        word_count = next_count
        if word_count >= target_words:
            break

    if not chosen:
        chosen = [sentences[0]]

    summary = " ".join(chosen)
    tokens = re.findall(r"\S+", summary)
    if len(tokens) > max_words:
        summary = " ".join(tokens[:max_words]).rstrip(",;:")
        if summary and summary[-1] not in ".!?":
            summary += "."

    return re.sub(r"\s+", " ", summary).strip()


def _looks_human_language(text: str) -> bool:
    """Filter out code-like, boilerplate, and low-signal docstrings."""
    if not text:
        return False
    words = text.split()
    if len(words) < 6 or len(words) > 80:
        return False
    lower = text.lower()
    if any(fragment in lower for fragment in LOW_VALUE_DOC_PATTERNS):
        return False
    if re.search(r"\b(args?|parameters?|returns?|examples?):", lower):
        return False
    alphabetic_ratio = sum(c.isalpha() for c in text) / max(len(text), 1)
    if alphabetic_ratio < 0.6:
        return False
    code_like = sum(1 for w in words if re.search(r"[_{}<>]|[A-Z][a-z]+[A-Z]|\w+::|\w+\.\w+", w))
    if code_like / len(words) > 0.25:
        return False

    # ── Additional pruning: syntactic echo, boilerplate, low-signal ──────
    for pattern in SYNTACTIC_ECHO_PATTERNS:
        if pattern.match(text.strip()):
            return False
    for pattern in BOILERPLATE_PATTERNS:
        if pattern.search(text):
            return False
    if any(phrase in lower for phrase in LOW_SIGNAL_KEYWORD_BLACKLIST):
        return False

    return True


def _collect_pairs(lang: str, max_samples: int) -> list[tuple[str, str]]:
    """
    Download code-comment pairs from CodeSearchNet for a single language.
    Returns curated (code, comment) tuples.
    """
    print(f"  ↓ Loading {lang} split from Hugging Face (code_search_net) …")
    ds = load_dataset("code_search_net", lang, split="train")

    pairs: list[tuple[str, str]] = []
    for row in ds:
        code = (row.get("func_code_string") or "").strip()
        raw_doc = (row.get("func_documentation_string") or "").strip()
        comment = _extract_summary(raw_doc)

        if not comment or not code:
            continue
        if not _looks_human_language(comment):
            continue

        pairs.append((code, comment))
        if len(pairs) >= max_samples:
            break

    rng = random.Random(RANDOM_SEED + sum(ord(ch) for ch in lang))
    rng.shuffle(pairs)
    return pairs[:max_samples]


def _normalize_code_key(code: str) -> str:
    compact = re.sub(r"\s+", " ", code).strip().lower()
    return hashlib.sha1(compact.encode("utf-8", errors="ignore")).hexdigest()


def _normalized_repo_key(sample: SampleRecord) -> str:
    if sample.repo:
        return f"{sample.source}:{sample.language}:{sample.repo.lower()}"
    if sample.path:
        return f"{sample.source}:{sample.language}:{sample.path.lower()}"
    return f"{sample.source}:{sample.language}:{_normalize_code_key(sample.code)[:16]}"


def _curate_record(
    *,
    code: str,
    raw_doc: str,
    language: str,
    repo: str,
    path: str,
    source: str,
    code_type: str = "function",
) -> SampleRecord | None:
    code = (code or "").strip()
    raw_doc = (raw_doc or "").strip()
    comment = _extract_summary(raw_doc)
    if not comment or not code:
        return None
    if not _looks_human_language(comment):
        return None
    return SampleRecord(
        code=code,
        comment=comment,
        language=language,
        repo=(repo or "").strip(),
        path=(path or "").strip(),
        source=source,
        code_type=code_type,
    )


def _collect_codesearchnet_pairs(lang: str, max_samples: int) -> list[SampleRecord]:
    print(f"  Loading {lang} split from Hugging Face (code_search_net)...")
    ds = load_dataset("code_search_net", lang, split="train")

    samples: list[SampleRecord] = []
    for row in ds:
        sample = _curate_record(
            code=row.get("func_code_string") or row.get("code") or "",
            raw_doc=row.get("func_documentation_string") or row.get("docstring") or "",
            language=lang,
            repo=row.get("repo") or "",
            path=row.get("path") or row.get("func_name") or "",
            source="codesearchnet",
        )
        if sample is None:
            continue
        samples.append(sample)
        if len(samples) >= max_samples:
            break

    rng = random.Random(RANDOM_SEED + 17 + sum(ord(ch) for ch in lang))
    rng.shuffle(samples)
    return samples[:max_samples]


def _collect_codexglue_pairs(lang: str, max_samples: int) -> list[SampleRecord]:
    print(f"  Loading {lang} split from Hugging Face (google/code_x_glue_ct_code_to_text)...")
    ds = load_dataset("google/code_x_glue_ct_code_to_text", lang, split="train")

