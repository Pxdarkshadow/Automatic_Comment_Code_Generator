"""
BPE Tokenizer and data pipeline for the GPT-style code-comment Transformer.

Components:
    BPETokenizer   – Custom Byte-Pair Encoding tokenizer trained on the joint code+comment
                     corpus.  Handles camelCase / snake_case splitting, shared vocabulary.
    FormattingPipe – Wraps (code, comment) pairs into the autoregressive prompt template:
                         Code:\n{code}\n\nComment: {comment}<eos>
    build_dataset  – Downloads CodeSearchNet data, trains BPE, encodes all sequences, splits
                     into train/val, and returns (tokenizer, train_seqs, val_seqs).
"""

